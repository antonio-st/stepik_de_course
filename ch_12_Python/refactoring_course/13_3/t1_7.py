# Датасет содержит записи с кодами ошибок. Напишите программу, которая принимает на вход общее количество записей
# и количество записей с ошибками, а затем выводит процент ошибок в датасете.

total_num_rec = int(input())
err_num_rec = int(input())

perc = (err_num_rec / total_num_rec) * 100

print(f"Процент ошибок: {perc} %")

