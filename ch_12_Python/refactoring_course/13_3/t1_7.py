#2
# Напишите программу, которая принимает на вход три целых числа и выводит их среднее значение с точностью
# до одного знака после запятой.
# Округление сделайте через функцию round, либо любой другой.

num4, num5, num6 = int(input()), int(input()), int(input())

numbers1 = [num4, num5, num6]

print(round(sum(numbers1) / len(numbers1), 1))

